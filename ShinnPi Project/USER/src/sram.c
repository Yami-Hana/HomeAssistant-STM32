/**
  * @brief  初始化FSMC外设
  * @param  None. 
  * @retval None.
 */

static void FSMC_ModeConfig(void)
{
	FSMC_NORSRAMTimingInitTypeDef  readTiming;
	FSMC_NORSRAMTimingInitTypeDef  writeTiming;
	FSMC_NORSRAMInitTypeDef  FSMC_NORSRAMInitStructure;
	
	//使能FSMC时钟
	RCC_AHBPeriphClockCmd(RCC_AHBPeriph_FSMC, ENABLE);
	
	//读
	readTiming.FSMC_AccessMode = FSMC_AccessMode_A;//选择匹配SRAM的模式
	readTiming.FSMC_AddressSetupTime = 0;//地址建立时间（ADDSET）为1个HCLK 1/72M=14ns
	readTiming.FSMC_AddressHoldTime = 0;//地址保持时间（ADDHLD）模式A未用到	 
	readTiming.FSMC_DataSetupTime = 2;//数据保持时间（DATAST）+ 1个HCLK = 3/72M=42ns(对EM的SRAM芯片)			  
	readTiming.FSMC_BusTurnAroundDuration = 0;//设置总线转换周期，仅用于复用模式的NOR操作
	readTiming.FSMC_CLKDivision = 0;//设置时钟分频，仅用于同步类型的存储器
	readTiming.FSMC_DataLatency = 0;//数据保持时间，仅用于同步型的NOR		
	
	//写
	writeTiming.FSMC_AccessMode = FSMC_AccessMode_A;//选择匹配SRAM的模式
	writeTiming.FSMC_AddressSetupTime = 0;//地址建立时间（ADDSET）为1个HCLK 1/72M=14ns
	writeTiming.FSMC_AddressHoldTime = 0;//地址保持时间（ADDHLD）模式A未用到	 
	writeTiming.FSMC_DataSetupTime = 2;//数据保持时间（DATAST）+ 1个HCLK = 3/72M=42ns(对EM的SRAM芯片)			  
	writeTiming.FSMC_BusTurnAroundDuration = 0;//设置总线转换周期，仅用于复用模式的NOR操作
	writeTiming.FSMC_CLKDivision = 0;//设置时钟分频，仅用于同步类型的存储器
	writeTiming.FSMC_DataLatency = 0;//数据保持时间，仅用于同步型的NOR	
	
	FSMC_NORSRAMInitStructure.FSMC_Bank = FSMC_Bank1_NORSRAM3;// 选择FSMC映射的存储区域： Bank1 sram3
	FSMC_NORSRAMInitStructure.FSMC_MemoryType =FSMC_MemoryType_SRAM;//设置要控制的存储器类型：SRAM类型   
	FSMC_NORSRAMInitStructure.FSMC_MemoryDataWidth = FSMC_MemoryDataWidth_16b;//存储器数据宽度：16位 
	FSMC_NORSRAMInitStructure.FSMC_ExtendedMode = FSMC_ExtendedMode_Enable;//不使用扩展模式，读写使用相同的时序 
	
	//SRAM未使用参数
	FSMC_NORSRAMInitStructure.FSMC_WaitSignalPolarity = FSMC_WaitSignalPolarity_Low;//设置等待信号的有效极性，仅用于同步类型的存储器
	FSMC_NORSRAMInitStructure.FSMC_WaitSignalActive = FSMC_WaitSignalActive_BeforeWaitState;//设置等待信号插入的时间，仅用于同步类型的存储器
	FSMC_NORSRAMInitStructure.FSMC_WriteOperation = FSMC_WriteOperation_Enable;//存储器写使能  
	FSMC_NORSRAMInitStructure.FSMC_DataAddressMux = FSMC_DataAddressMux_Disable;//设置地址总线与数据总线是否复用，仅用于NOR 
	FSMC_NORSRAMInitStructure.FSMC_WrapMode = FSMC_WrapMode_Disable;//设置是否支持把非对齐的突发操作，仅用于同步类型的存储器
	FSMC_NORSRAMInitStructure.FSMC_BurstAccessMode =FSMC_BurstAccessMode_Disable;//设置是否使用突发访问模式，仅用于同步类型的存储器
	FSMC_NORSRAMInitStructure.FSMC_AsynchronousWait=FSMC_AsynchronousWait_Disable;//设置是否使能等待信号，仅用于同步类型的存储器
	FSMC_NORSRAMInitStructure.FSMC_WaitSignal = FSMC_WaitSignal_Disable;//不使用等待信号  		
	FSMC_NORSRAMInitStructure.FSMC_WriteBurst = FSMC_WriteBurst_Disable;//突发写操作 
	
	//读时序配置
	FSMC_NORSRAMInitStructure.FSMC_ReadWriteTimingStruct = &readTiming;
	//读写同样时序，使用扩展模式时这个配置才有效
	FSMC_NORSRAMInitStructure.FSMC_WriteTimingStruct = &writeTiming; 

	FSMC_NORSRAMInit(&FSMC_NORSRAMInitStructure);  //初始化FSMC配置
	FSMC_NORSRAMCmd(FSMC_Bank1_NORSRAM3, ENABLE);  // 使能BANK		
	
}

void SRAM_Init(void)
{
	SRAM_GPIO_Config();
	FSMC_ModeConfig();
}


